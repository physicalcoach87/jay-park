-- 013_period_ref_basis.sql — 주기화 강도 기준(최근 3경기) 스냅샷 저장
--
-- 목적: 코치가 %로 강도를 설정하면, "그 시점의 최근 3경기 평균"을 함께 저장.
--   → 지난 날은 준비 당시 기준으로 고정(%가 흔들리지 않음)
--   → 새 경기가 반영되면 앞으로 짜는 계획만 새 기준을 따름
--   → 각 계획이 어떤 경기 기준으로 짜였는지 기록에 남음
--
-- ref_basis 구조(JSONB):
--   {
--     "pct":     { "td":100, "hsr":100, "spr":100, "acc":100, "rhie":100 },
--     "avg":     { "td":10956, "hsr":..., "sprint":..., "accel":..., "rhie":... },
--     "matches": [ { "date":"2026-07-05", "opp":"전남드래곤즈" }, ... ],
--     "saved_at":"2026-07-10T..."
--   }
-- ============================================================

ALTER TABLE block_days ADD COLUMN IF NOT EXISTS ref_basis jsonb;

-- 기존 행에는 ref_basis가 없음 → 앱이 자동으로 "기준 미기록(구 데이터)"으로 처리하고
-- 편집·재저장 시 현재 기준을 스냅샷으로 남김. 강제 백필은 하지 않음(과거 기준을 알 수 없으므로).
